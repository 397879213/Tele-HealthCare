    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         int selectedRow = patientDatabase.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a Row!");
            return;
        }
        
        Employee patient = (Employee) patientDatabase.getValueAt(selectedRow, 0);
        
        Date date = new Date();
        patient.getMedicalRecord().setDischarge("Yes");
        patient.getMedicalRecord().setDischargeDate(date);
        
        DischargedPatient dispat = enterprise.getDischargedPatientDirectory().createPatient();
        dispat.setPatientName(patient.getName());
        dispat.setAge(patient.getAge());
        dispat.setPatientId(Integer.toString(patient.getId()));
        dispat.setPrimaryDoctor(patient.getPrimaryDoctor());
        dispat.setMedicalRecord(patient.getMedicalRecord());
        
        
        populateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

