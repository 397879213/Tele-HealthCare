    public void populateTable(){
         DefaultTableModel dtm = (DefaultTableModel)patientDatabase.getModel();
        
        int rowcount = patientDatabase.getRowCount();
        
        for(int i =rowcount-1;i>=0;i--){
        dtm.removeRow(i);
    }
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof PatientOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            for(Employee patient: org.getEmployeeDirectory().getEmployeeList()){
                Object row[] = new Object[8];
                row[0]= patient;
                row[1]= patient.getPrimaryDoctor();
                row[2] =patient.getMedicalRecord().getAdmissionDate();
                row[3] = patient.getMedicalRecord().getResumeWork();
                row[4] = patient.getMedicalRecord().getIsOperationPerformed();
                row[5] = patient.getMedicalRecord().getTreatedFor();
                row[6] = patient.getMedicalRecord().getDischarge()== null ? "Not Yet" : patient.getMedicalRecord().getDischarge();
                row[7] = patient.getMedicalRecord().getDischargeDate() == null ? "Not Yet" : patient.getMedicalRecord().getDischargeDate();
                dtm.addRow(row);
            }
        }
        
        
        
    }

