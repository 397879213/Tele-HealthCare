    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        doctor = (Employee)doctorCombo.getSelectedItem();
        patient = (Employee)patientCombo.getSelectedItem();
         if(patientAccount==null){
            for(UserAccount ua:patOrg.getUserAccountDirectory().getUserAccountList()){
                if(ua.getEmployee().getName().equalsIgnoreCase(patient.getName())){
                    patientAccount=ua;
                }
            }
            
            
         }    
        
        AppointmentWorkRequest request = new AppointmentWorkRequest();
        request.setMessage("Book Appointment");
        request.setSender(patientAccount);
        request.setStatus("Sent");
        request.setRequestDate(dateChooser.getDate());

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof DoctorOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }

        populateRequestTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = request.getMessage();
            row[1] = request.getReceiver();
            row[2] = request.getStatus();
            String result = ((AppointmentWorkRequest) request).getResult();
            row[3] = result == null ? "Waiting" : result;
            
            model.addRow(row);
        }
    }
