    public void populatePatientCombo(){
          patOrg = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof PatientOrganization){
                patOrg = organization;
                
                break;
            }
        }
       
        
        if(patOrg!=null){
            for(Employee employee: patOrg.getEmployeeDirectory().getEmployeeList()){
                patientCombo.addItem(employee);
            }
        }
        
    }

    private void populateDoctorCombo(){
         Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof DoctorOrganization){
                org = organization;
                break;
            }
        }
        
        if(org!=null){
            for(Employee employee: org.getEmployeeDirectory().getEmployeeList()){
                doctorCombo.addItem(employee);
            }
        }
    }
